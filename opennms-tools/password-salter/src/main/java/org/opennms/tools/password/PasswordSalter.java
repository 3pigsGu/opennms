package org.opennms.tools.password;

import org.jasypt.util.password.PasswordEncryptor;
import org.jasypt.util.password.StrongPasswordEncryptor;

public class PasswordSalter {
	public static void main(String[] args) {
		if (args.length != 1) {
			System.out.println("You must specify a password on the command line.");
			System.exit(1);
		}
		PasswordEncryptor pe = new StrongPasswordEncryptor();
		System.out.println(pe.encryptPassword(args[0]));
	}
}
